public class BulkUnityConversion {

      public static double ltToCubCm(double lt) {
            return lt * 1000;
      }

      public static double mCubToL(double mCub) {
            return mCub * 1000;
      }

      public static double mCubToCubft(double mCub) {
            return  mCub * 35.32;
      }

      public static double galToCubInch(double gal) {
            return gal * 231;
      }
      
      public static double galToLt(double gal) {
            return gal * 3.785;
      }
}